public class LeftShift {
        public static void main(String[] args) {
            int value = 44; // Binary: 0000 0101
            int shiftedValue = value << 2; // Shift left by 2 positions

            System.out.println("Original value: " + value); // Output: 5
            System.out.println("Shifted value: " + shiftedValue); // Output: 20
        }
    }

